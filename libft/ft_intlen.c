#include "libft.h"

size_t	ft_intlen(int nb)
{
	size_t			i;
	unsigned int	n;

	i = 0;
	if (nb == 0)
		return (1);
	if (nb < 0 && i == 0)
	{
		n = nb * -1;
		i++;
	}
	else
		n = nb;
	while (n > 0)
	{
		n /= 10;
		i++;
	}
	return (i);
}
