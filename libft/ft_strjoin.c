#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*ret;
	size_t	len1;
	size_t	len2;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	if (!(ret = malloc(len1 + len2 + 1)))
		return (NULL);
	ft_strcpy(ret, s1);
	ft_strcpy(ret + len1, s2);
	return (ret);
}
