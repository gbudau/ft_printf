#include "libft.h"

size_t	ft_nblen(long nb)
{
	size_t			i;
	unsigned long	n;

	i = 0;
	n = nb < 0 ? -nb : nb;
	while (n)
	{
		n /= 10;
		i++;
	}
	if (!i)
		return (1);
	if (nb < 0)
		i++;
	return (i);
}
