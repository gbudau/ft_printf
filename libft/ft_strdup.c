#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*ret;
	size_t	len;

	len = ft_strlen(s1);
	if (!(ret = malloc(len + 1)))
		return (NULL);
	ft_strcpy(ret, s1);
	return (ret);
}
